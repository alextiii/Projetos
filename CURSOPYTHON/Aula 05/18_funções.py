"""
FUNÇÔES
Uma forma de organizar o código e garantir que ele 
possa ser reutilizado. Ideal que cada função seja 
responsável por uma tarefa...
"""

# Função diz oi
def dizer_Oi():
    print('Oi')
dizer_Oi()   
# Função canta parabéns
def dizer_parabens():
    print('Parabéns para você')
dizer_parabens()   
# Função soma 2 valores
def somar2valores():
    valor1=int(input(f'Digite um valor:'))
    valor2=int(input(f'Digite o segundo valor:'))
    print((valor1)+(valor2))
somar2valores()



