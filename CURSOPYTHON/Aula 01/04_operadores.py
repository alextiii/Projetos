"""
Operadores Aritméticos e lógicos:

+  Adição           *  Multiplicação
-  Subtração        ** Exponenciação

/  Divisão
// Retorna o valor inteiro da divisão
%  Retorna o resto da divisão

>   Maior           ==  Igual
<   Menor           !=  Diferente

>=  Maior ou igual
<=  Menor ou igual
=   Atribuição
"""

a = 10
b = 3

print(a + b)   # adição
print(a - b)   # subtrair

print(a * b)   # multiplicar
print(a ** b)  # exp

print(a / b)   # divisão 3.3333
print(a // b)  # divisão com valor inteiro 3
print(a % b)   # resto da divisão 1 ele divide por 3 e multiplica por 3 e para 10 falta 1
print(a > b)   # [a] é maior que [b]?
print(a <= b)  # [a] é menor ou igual [b]?
print(a == b)  # [a] é igual [b]?
print(a != b)  # [a] é diferente de [b]?

